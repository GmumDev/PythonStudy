'''
# na = 20 인풋으로
# nb = 5 인풋으로
# 더하기의 결과값 result1
# 빼기 결과값 result2
# 곱하기 결과값 result3
# 나누기 결과값 result4
'''
na = float(input("실수 입력 : "))
nb = float(input("실수 입력 : "))
result1 = na + nb
result2 = na - nb
result3 = na * nb
result4 = na / nb

print(na, "+", nb, "=", result1)
print(na, "-", nb, "=", result2)
print(na, "×", nb, "=", result3)
print(na, "÷", nb, "=", result4)